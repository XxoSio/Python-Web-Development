# 주석(Comments)
# 1. 코드에 설명을 추가하고 싶을 때
# 2. 코드를 실행하고 싶지 않을 때

# 숫자 자료형
# 1. 정수형 : 소수점이 없는 수
print(1, 3, 0, -1, sep="")
# sep="" : 다음 데이터 출력 시 자동 띄워쓰기를 붙여쓰기로

# 2. 실수형 : 소수점이 있는 수
print(1.1, 3.1, 0, -1.4)

# 3. 문자열 자료형
# '' or ""
print("파이썬 너무 재밌다!", end='')
print('파이썬 정말 재밌자너!')
# end="" : 문장이 끝난 후 자동 줄 바꿈을 붙여쓰기로 변경
print('"개 짖는 소리 좀 안나게 해라"라고 그는 외쳤다.')

# 불린형 자료형
# Ture or False
print(True)
print(False)